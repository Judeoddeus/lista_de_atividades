/*
Laço de repetições são blocos dentro de nosso código que será executado até que a condi~ção seja
falsa, no caso do FOR qle possui 3 partes:
for(inicialização da variável; condição; incremento)
for(let i = 0; i <= 10; i++ )
inicialização da variável --> aqui colocamos um valor inicial para a variável que servirá para
controle de nosso laço. Normalmente o nome dessa variável é i (increment) porém não há problema
em usar outro nome.
condição --> teste que será realizado em cada nova passagem do laço, o conteúdo
dentro do laço será executado enquabnto a condiçao retornar verdadeiro.
incremento --> aumento no número da variável. i++ é a mesma coisa que i = i +1
*/